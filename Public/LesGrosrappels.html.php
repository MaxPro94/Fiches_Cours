        <h2>Les abréviations a connaitre :</h2>
        <ul>
            <li><span class="mot-clef">C.R.U.D</span></li>
            <p>C : Create / R : Read / U : Update / D : Delete</p>
            <P>CRUD est un acronyme qui désigne les quatre opérations de base effectuées sur les données dans un système
                de
                gestion de base de données (SGBD) ou tout autre type de système qui manipule des données. Les opérations
                CRUD sont les suivantes :</P>
            <br>
            <li><span class="mot-clef">S.G.B.D</span></li>
            <p>S : Système / G : Gestion / B : Base / D : Données</p>
            <p>Un SGBD, ou Système de Gestion de Base de Données, est un logiciel qui offre un ensemble de services
                permettant de créer, gérer et manipuler des bases de données. Les bases de données sont des collections
                structurées d'informations, organisées de manière à faciliter l'accès, la gestion et la mise à jour des
                données. Les SGBD jouent un rôle crucial dans le domaine de la gestion de l'information au sein des
                applications logicielles et des systèmes d'information.</p>
            <br>
            <p>Exemples de SGBD bien connus comprennent MySQL, PostgreSQL, Oracle Database, Microsoft SQL Server,
                MongoDB
                (un type de SGBD NoSQL), et SQLite, entre autres. Ces systèmes facilitent la gestion efficace et
                sécurisée
                des données, ce qui est essentiel dans le développement de logiciels et la gestion des systèmes
                d'information.</p>
            <br>
            <li><span class="mot-clef">M.C.D</span></li>
            <p>Un MCD, ou Modèle Conceptuel de Données, est un type de schéma qui représente les concepts et les
                relations
                entre eux dans un domaine d'application particulier. Il s'agit d'un modèle abstrait qui vise à décrire
                la
                structure logique des données, indépendamment de la manière dont elles seront stockées physiquement dans
                une
                base de données.</p>
            <br>
            <li><span class="mot-clef">D.O.M</span></li>
            <p>Un DOM est le document object modele donc de HTML ou le CSS</p>
            <br>
            <li><span class="mot-clef">B.E.M</span></li>
            <p>Bloc Element Modification</p>
            <p>Méthodologie de codage afin de rendre le code CSS compréhensible par rapport a la structure du site web.
            </p>
            <p>En somme cela revient a donner des nom de class trés parlant et en faisant en sorte d'y mettre une sorte
                de chemin CDN.</p>
            <br>
            <p>Les principaux éléments d'un MCD incluent :</p>
            <ol>
                <li><span class="mot-clef">Entités :</span></li>
                <p>Ce sont des objets, des choses ou des concepts du monde réel qui sont identifiables et qui ont des
                    caractéristiques que l'on souhaite enregistrer. Chaque entité est généralement représentée par un
                    rectangle dans le MCD.</p>
                <br>
                <li><span class="mot-clef">Attributs :</span></li>
                <p>Ce sont les propriétés ou caractéristiques des entités. Les attributs sont souvent représentés à
                    l'intérieur des rectangles d'entité et décrivent des informations spécifiques sur chaque entité.</p>
                <br>
                <li><span class="mot-clef">Relations :</span></li>
                <p>Ce sont les associations ou liens entre différentes entités. Les relations sont généralement
                    représentées
                    par des lignes qui connectent les entités concernées. Une relation peut avoir des attributs propres,
                    indiquant des détails spécifiques sur la façon dont les entités sont liées.</p>
            </ol>
            <p>Le but du MCD est de fournir une vue claire et abstraite des données qui sera utilisée comme base pour
                concevoir la structure d'une base de données. Le modèle conceptuel de données aide les concepteurs à
                comprendre les exigences du système et à définir la manière dont les données doivent être organisées et
                interconnectées.</p>
            <br>
            <p>Il est important de noter que le MCD est une étape dans le processus de conception de bases de données.
                Une fois que le MCD est établi, il peut être transformé en un Modèle Logique de Données (MLD) qui prend
                en compte des détails supplémentaires, comme les types de données, les clés primaires et étrangères, et
                d'autres contraintes spécifiques à la mise en œuvre d'une base de données relationnelle.</p>
