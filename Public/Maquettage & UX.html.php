        <h1>A quoi sert le maquettage ?</h1>
        <p>Le maquettage est une étape importante dans la creation d'un site Web. Elle permet de definir une charte
            esthetique pour la construction du site.</p>
        <p>La ligne temporel a suivre est la suivante:</p>
        <p><span>Introduction</span> → <span>Inspiration</span> → <span>Conception</span> → <span>Optimisation</span>
        </p>
        <br>
        <h2>Demarrage d'un projet de site Web</h2>
        <p>Pour bien demarrer nous allons d'abord nous renseigner sur le secteur d'activité de notre client (Banque,
            BTP,
            Commerce etc..), afin de pouvoir au mieux comprendre ses besoins.</p>
        <br>
        <p>Nous mettrons ensuite en place une analyse concurrentielle, nous irons comparer différents site web du meme
            secteur d'activité afin de voir, les plus et les moins des concurrents et voir ce qui est normé en raison de
            l'activité.</p>
        <br>
        <p>Voici venu l'heure de la charte graphique qui se presenteras de plus souvent comme cela: </p>
        <img src="assets/img/Maquettage & UX/ExempleCharteGraph.png" alt="Charte graphique">
        <p>Cette charte graphique comprend:</p>
        <ul>
            <li>Zones de construction (logotype, titre, expression graphique, navigation, contenu, pied de page).</li>
            <li>Univers graphique (logotype, polices de titre et de texte, aplat de couleur, blanc tournant).</li>
            <li>Polices de navigation.</li>
            <li>Polices de titre (titre en texte-image, titre en HTML).</li>
            <li>Couleur (code RVB, HTML).</li>
            <li>Navigation.</li>
            <li>Les types de visuel (encadré, détouré).</li>
            <li>Les pictogrammes.</li>
            <li>Les tableaux.</li>
            <li>Les graphiques.</li>
            <li>Les illustrations.</li>
            <li>Le fond de page.</li>
        </ul>
        <p>Il existe toutes fois quelques normes en design, comme ne jamais mettre de Blanc pure ni de Noir pure se qui
            rendrait le site pas agréable a l'oeil.</p>
        <p>Nous utiliserons des code couleur comme #222222 pour amener un noir un peur moins profond et #efefef pour un
            blanc un moins eclatant.</p>
        <p>Il existe plusieurs outils ou application pour nous aider dans la construction d'une charte graphique:</p>
